use basic_text::TextString;

/// Various miscellaenous testcases.
#[test]
fn basic_text_string_basics() {
    assert_eq!(TextString::from_text("".to_owned()).unwrap().as_str(), "");
    assert_eq!(
        TextString::from_text("hello world!".to_owned())
            .unwrap()
            .as_str(),
        "hello world!"
    );
    assert_eq!(
        TextString::from_text("\n".to_owned()).unwrap().as_str(),
        "\n"
    );
    assert_eq!(
        TextString::from_text("\t".to_owned()).unwrap().as_str(),
        "\t"
    );
    assert_eq!(TextString::from_text(" ".to_owned()).unwrap().as_str(), " ");
    assert_eq!(
        TextString::from_text("[({\"\\~@".to_owned())
            .unwrap()
            .as_str(),
        "[({\"\\~@"
    );
    assert_eq!(
        TextString::from_text("\t\n \u{a0}".to_owned())
            .unwrap()
            .as_str(),
        "\t\n \u{a0}"
    );
    assert_eq!(
        TextString::from_text("\u{2011}\u{ad}".to_owned())
            .unwrap()
            .as_str(),
        "\u{2011}\u{ad}"
    );
    assert_eq!(
        TextString::from_text("A\u{34f}\u{200c}\u{200d}\u{2060}".to_owned())
            .unwrap()
            .as_str(),
        "A\u{34f}\u{200c}\u{200d}\u{2060}"
    );
    assert_eq!(
        TextString::from_text("\u{200e}\u{200f}\u{61c}".to_owned()).unwrap(),
        "\u{200e}\u{200f}\u{61c}"
    );
    assert_eq!(
        TextString::from_text("\u{fffd}".to_owned()).unwrap(),
        "\u{fffd}"
    );
    assert_eq!(
        TextString::from_text("\u{10fffd}\u{fdfa}\u{2800}".to_owned()).unwrap(),
        "\u{10fffd}\u{fdfa}\u{2800}"
    );
    assert_eq!(
        TextString::from_text("\u{e0000}\u{e0002}".to_owned())
            .unwrap()
            .as_str(),
        "\u{34f}\u{e0000}\u{34f}\u{e0002}\u{34f}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{34f}\u{200c}\u{200d}\u{2060}").as_str(),
        "\u{34f}\u{200c}\u{200d}\u{2060}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{200c}\u{200d}\u{2060}").as_str(),
        "\u{34f}\u{200c}\u{200d}\u{2060}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{200d}").as_str(),
        "\u{34f}\u{200d}\u{34f}"
    );
}

/// Unassigned codepoints are isolated from string boundaries with CGJ.
#[test]
fn basic_text_string_unassigned() {
    // Isolate unassigned scalar values with CGJ's.
    assert_eq!(
        TextString::from_text_lossy("\u{378}").as_str(),
        "\u{34f}\u{378}\u{34f}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{378}\u{378}").as_str(),
        "\u{34f}\u{378}\u{34f}\u{378}\u{34f}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{34f}\u{378}\u{34f}").as_str(),
        "\u{34f}\u{378}\u{34f}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{34f}\u{378}\u{34f}\u{378}\u{34f}").as_str(),
        "\u{34f}\u{378}\u{34f}\u{378}\u{34f}"
    );

    // Test U+FFFFF instead of U+378. It's unassigned but also replaced.
    assert_eq!(
        TextString::from_text_lossy("\u{fffff}").as_str(),
        "\u{fffd}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{fffff}\u{fffff}").as_str(),
        "\u{fffd}\u{fffd}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{34f}\u{fffff}\u{34f}").as_str(),
        "\u{34f}\u{fffd}\u{34f}"
    );
    assert_eq!(
        TextString::from_text_lossy("\u{34f}\u{fffff}\u{34f}\u{fffff}\u{34f}").as_str(),
        "\u{34f}\u{fffd}\u{34f}\u{fffd}\u{34f}"
    );
}

